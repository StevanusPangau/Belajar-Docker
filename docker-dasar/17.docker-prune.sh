# Perintah Prune
# Perintah prune digunakan untuk menghapus container, image, volume, dan network yang tidak digunakan oleh container yang aktif.

# Perintah untuk menghapus container yang tidak digunakan
docker container prune

# Perintah untuk menghapus image yang tidak digunakan
docker image prune

# Perintah untuk menghapus volume yang tidak digunakan
docker volume prune

# Perintah untuk menghapus network yang tidak digunakan
docker network prune

# Perintah untuk menghapus container, image, volume, dan network yang tidak digunakan
docker system prune